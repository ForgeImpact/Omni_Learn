# Install Firebase CLI if not already installed
npm install -g firebase-tools

# Login to Firebase
firebase login

# Initialize Firebase in your project directory
firebase init

# Select these Firebase services:
# - Firestore: For storing user profiles, learning content, and progress
# - Authentication: For user management across devices
# - Storage: For educational content like videos and simulations
# - Hosting: For web deployment
# - Functions: For backend functionality
